// exposes a tiny API on `window.I18N`:
//
//   I18N.t(key, params?)   -> localized string ({n}-style placeholders)
//   I18N.apply(root?)      -> fill [data-i18n] / [data-i18n-aria-label] nodes
//   I18N.setLanguage(code) -> set the active locale ("system" follows the OS)
